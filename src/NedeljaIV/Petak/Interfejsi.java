package NedeljaIV.Petak;

public class Interfejsi {

    /*
     * Interfejsi
     * 
     * Interfejs je abstraktna klasa sa svim javnim abstraktnim metoda
     * i sa svim javnim statickim FINALNIM promenjivima.
     * 
     *
     * - Interfejs je klasa koja skuplja u jednu logicku celinu sve metode
     *   koje se ticu jedne "oblasti". 
     * 
     * public interface <idetifikator> {
     *  ...
     *  
     * 
     * 
     * }
     * 
     * - Interfejsi ne mogu imati konstruktore!
     * 
     * - Kod interfejsa ne moramo da navodimo kljucne reci public i abstract
     *   za metode 
     *   public abstract void f(); <---> void f();
     * 
     * - Kod interfejsa ne moramo da navodimo kljucne reci public, static
     *   i final za promenjive
     *   public static final int x; <---> int x;
     * 
     * - Posto se interfejs ponasa kao apstraktna klasa, ona ne moze biti
     *   Izveden tip vec mora biti Nominalni tip.
     * 
     * - (Neinterfejsne) klase IMPLEMENTUJU interfejs, pomocu kljucne reci
     *   implements
     * 
     * - Jedan interfejs prosiruje drugi interfejs (extends)
     * 
     *  Interface I;
     *  Class A;
     *  Class A implements I;
     * 
     *  Interface I2
     *  Interface I2 extends I;
     * 
     * - Jedna klasa moze da implementuje vise interfejsa
     * Class A implements I1, I2, ... {}
     * 
     * - Putem interfejsa mozemo da realizujemo dijamantsku strukturu u javi!
     * 
     * 
     *   
     * 
     * 
     * 
     * 
     */
    
}
